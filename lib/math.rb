def addition(num1, num2)
  sum = num1 + num2
  sum
end

def subtraction(num1, num2)
  diff = num1 - num2
  diff
end

def division(num1, num2)
  result = num1 / num2
  result
end

def multiplication(num1, num2)
  product = num1 * num2
  product
end

def modulo(num1, num2)
  result = num1 % num2
  result
end

def square_root(num)
  result = Math.sqrt(num)
  result
end